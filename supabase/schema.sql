-- Catalog + settings: world-readable, client-managed writes.
do $$
declare t text;
begin
  foreach t in array array['categories','programs','promotions','showroom','contacts']
  loop
    execute format('drop policy if exists "%1$s_read"  on public.%1$s;', t);
    execute format('drop policy if exists "%1$s_write" on public.%1$s;', t);
    -- public read
    execute format(
      'create policy "%1$s_read" on public.%1$s
         for select to anon, authenticated using (true);', t);
    -- client-managed write (admin lives client-side -> trust anon; harden later)
    execute format(
      'create policy "%1$s_write" on public.%1$s
         for all to anon, authenticated using (true) with check (true);', t);
  end loop;
end$$;

-- Orders: anyone can CREATE (public site), client (admin) can read/manage.
drop policy if exists "commands_insert" on public.commands;
drop policy if exists "commands_manage" on public.commands;
create policy "commands_insert" on public.commands
  for insert to anon, authenticated with check (true);
create policy "commands_manage" on public.commands
  for all to anon, authenticated using (true) with check (true);

drop policy if exists "custom_orders_insert" on public.custom_orders;
drop policy if exists "custom_orders_manage" on public.custom_orders;
create policy "custom_orders_insert" on public.custom_orders
  for insert to anon, authenticated with check (true);
create policy "custom_orders_manage" on public.custom_orders
  for all to anon, authenticated using (true) with check (true);

-- Admin account: the app reads & edits it client-side (see SECURITY note).
drop policy if exists "admin_account_rw" on public.admin_account;
create policy "admin_account_rw" on public.admin_account
  for all to anon, authenticated using (true) with check (true);

