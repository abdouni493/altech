-- Programs: filter by category, newest-first ordering, instant text search.
create index if not exists idx_programs_category    on public.programs (category_id);
create index if not exists idx_programs_created_desc on public.programs (created_at desc);
create index if not exists idx_programs_name_trgm    on public.programs using gin (name gin_trgm_ops);
create index if not exists idx_programs_desc_trgm    on public.programs using gin (description gin_trgm_ops);

-- Promotions: filter by category/base program, sort by end date, and a PARTIAL
-- index that only covers active promos (what the public site actually lists).
create index if not exists idx_promotions_category     on public.promotions (category_id);
create index if not exists idx_promotions_base_program on public.promotions (base_program_id);
create index if not exists idx_promotions_end_date     on public.promotions (end_date);
create index if not exists idx_promotions_active       on public.promotions (active) where active;
create index if not exists idx_promotions_created_desc on public.promotions (created_at desc);

-- Commands: dashboard filters by status/type, lists newest-first.
create index if not exists idx_commands_status       on public.commands (status);
create index if not exists idx_commands_type         on public.commands (type);
create index if not exists idx_commands_program      on public.commands (program_id);
create index if not exists idx_commands_created_desc on public.commands (created_at desc);
create index if not exists idx_commands_status_created on public.commands (status, created_at desc);

-- Custom orders: newest-first.
create index if not exists idx_custom_orders_created_desc on public.custom_orders (created_at desc);

-- Categories: stable ordering.
create index if not exists idx_categories_created on public.categories (created_at);

