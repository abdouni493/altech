-- WHAT IT CREATES
--   • 8 tables matching the app data model (camelCase in app -> snake_case here)
--   • Foreign keys with safe ON DELETE behaviour (orders keep their snapshot)
--   • Performance indexes (B-tree on FKs/status/dates, partial on active promos,
--     GIN + pg_trgm for instant text search on programs)
--   • 2 PUBLIC storage buckets: `media` (program/promotion images) + `logos`
--     (showroom logo). The app stores the PUBLIC URL of each upload in the row.
--   • updated_at triggers
--   • Row Level Security + policies
--   • Seed data identical to the app's mock data (so first load is seamless)
--
-- ⚠️  SECURITY MODEL — READ THIS
--   This app authenticates the admin CLIENT-SIDE (it compares a typed password
--   against a row) and talks to Supabase with the PUBLIC anon key only — there
--   is no Supabase Auth session. The anon key is *designed* to be public; the
--   real protection layer is RLS. But because every request (public visitor AND
--   admin) arrives as the same `anon` role, RLS cannot tell them apart, so the
--   write policies below TRUST THE CLIENT. That matches the app as it exists.
--   To truly lock down admin writes, adopt Supabase Auth and switch the write
--   policies from `to anon` to `to authenticated` (see the HARDENING block at
--   the bottom of this file). Until then, do not store anything sensitive here.
-- =============================================================================

