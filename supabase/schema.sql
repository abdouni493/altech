-- Promotions ----------------------------------------------------------------
create table if not exists public.promotions (
  id                 text primary key,
  base_program_id    text references public.programs(id) on delete set null,
  name               text not null,
  description        text not null default '',
  image              text not null default '',  -- PUBLIC URL from `media` bucket
  category_id        text references public.categories(id) on delete set null,
  price_online       integer not null default 0 check (price_online      >= 0),
  price_offline      integer not null default 0 check (price_offline     >= 0),
  new_price_online   integer not null default 0 check (new_price_online  >= 0),
  new_price_offline  integer not null default 0 check (new_price_offline >= 0),
  end_date           timestamptz,
  demo_url_enabled   boolean not null default false,
  demo_url           text not null default '',
  active             boolean not null default true,
  created_at         timestamptz not null default now(),
  updated_at         timestamptz not null default now()
