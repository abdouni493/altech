-- Categories ----------------------------------------------------------------
create table if not exists public.categories (
  id          text primary key,
  name        text not null,
  created_at  timestamptz not null default now()
);

-- Programs ------------------------------------------------------------------
create table if not exists public.programs (
  id                text primary key,
  name              text not null,
  description       text not null default '',
  image             text not null default '',  -- PUBLIC URL from `media` bucket
  price_online      integer not null default 0 check (price_online  >= 0),
  price_offline     integer not null default 0 check (price_offline >= 0),
  demo_url_enabled  boolean not null default false,
  demo_url          text not null default '',
  category_id       text references public.categories(id) on delete set null,
  created_at        timestamptz not null default now(),
  updated_at        timestamptz not null default now()
