-- Storage policies on storage.objects (RLS is already enabled by Supabase).
drop policy if exists "media_logos_public_read"   on storage.objects;
drop policy if exists "media_logos_anon_insert"    on storage.objects;
drop policy if exists "media_logos_anon_update"    on storage.objects;
drop policy if exists "media_logos_anon_delete"    on storage.objects;

create policy "media_logos_public_read" on storage.objects
  for select to anon, authenticated
  using (bucket_id in ('media','logos'));

create policy "media_logos_anon_insert" on storage.objects
  for insert to anon, authenticated
  with check (bucket_id in ('media','logos'));

create policy "media_logos_anon_update" on storage.objects
  for update to anon, authenticated
  using (bucket_id in ('media','logos'))
  with check (bucket_id in ('media','logos'));

create policy "media_logos_anon_delete" on storage.objects
  for delete to anon, authenticated
  using (bucket_id in ('media','logos'));

