-- Commands (online/offline orders) ------------------------------------------
create table if not exists public.commands (
  id            text primary key,
  client_name   text not null,
  phone         text not null,
  wilaya        text not null,
  program_id    text references public.programs(id) on delete set null,
  type          text not null default 'online'  check (type   in ('online','offline')),
  price         integer not null default 0 check (price >= 0),
  status        text not null default 'pending' check (status in ('pending','accepted','cancelled')),
  image         text not null default '',  -- snapshot of program image at order time
  program_name  text not null default '',  -- snapshot of program name at order time
