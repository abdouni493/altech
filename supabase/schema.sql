create extension if not exists pgcrypto;   -- gen_random_uuid(), digests
create extension if not exists pg_trgm;    -- trigram index for fast ILIKE search

-- ---------------------------------------------------------------------------
-- 1. Shared helper: keep updated_at fresh on every UPDATE
-- ---------------------------------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

